# Database schema description for the LLM
_DB_SCHEMA = """
Tables and Columns:

1. customers
   - customer_id (INTEGER, PRIMARY KEY)
   - first_name (VARCHAR)
   - last_name (VARCHAR)
   - email (VARCHAR)
   - phone (VARCHAR)
   - date_of_birth (DATE)
   - address (VARCHAR)
   - city (VARCHAR)
   - state (VARCHAR)
   - zip_code (VARCHAR)

2. policies
   - policy_id (INTEGER, PRIMARY KEY)
   - customer_id (INTEGER, FK → customers.customer_id)
   - policy_type (VARCHAR) — values: 'auto', 'home', 'life', 'travel'
   - coverage_amount (DECIMAL)
   - premium_amount (DECIMAL)
   - start_date (DATE)
   - end_date (DATE)
   - status (VARCHAR) — values: 'active', 'expired', 'cancelled', 'suspended'

3. claims
   - claim_id (INTEGER, PRIMARY KEY)
   - policy_id (INTEGER, FK → policies.policy_id)
   - claim_amount (DECIMAL)
   - claim_date (DATE)
   - claim_status (VARCHAR) — values: 'pending', 'approved', 'denied', 'processing'
   - claim_type (VARCHAR)
   - description (TEXT)
"""

_SQL_SYSTEM_PROMPT = f"""You are a SQL expert. Given a natural language question about
insurance data, generate a DuckDB-compatible SELECT query.

{_DB_SCHEMA}

Rules:
1. Only use tables and columns from the schema above.
2. Use DuckDB SQL syntax (YEAR(), MONTH(), etc. for date functions).
3. Use JOINs when data from multiple tables is needed.
4. Limit results to 20 rows unless the user specifies otherwise.
5. Return ONLY the SQL query — no explanation, no markdown fences."""

# Will be set by init_crm_tools() when the orchestrator passes the LLM
_llm = None


def init_crm_tools(llm: object) -> None:
    """Provide the LLM instance needed by the analytics tool."""
    global _llm
    _llm = llm


@tool
def crm_analytics(question: str) -> str:
    """Run a complex analytics query against the insurance CRM database.

    Converts a natural-language question into SQL and executes it against the
    DuckDB database containing customers, policies, and claims.

    Use this tool for aggregate queries, comparisons, trend analysis, or any
    question that the other CRM tools cannot answer directly.

    Args:
        question: A natural language question about insurance data.

    Returns:
        Formatted query results as a table string.
    """
    if _llm is None:
        return "Error: analytics LLM not initialized."

    from langchain_core.messages import HumanMessage, SystemMessage

    try:
        response = _llm.invoke(
            [SystemMessage(content=_SQL_SYSTEM_PROMPT), HumanMessage(content=question)]
        )
        sql_query = response.content.strip()

        # Strip markdown code fences if present
        if sql_query.startswith("```"):
            lines = sql_query.split("\n")
            sql_query = "\n".join(
                l for l in lines if not l.strip().startswith("```")
            ).strip()

        if not sql_query.upper().startswith("SELECT"):
            return f"Error: LLM generated a non-SELECT statement — refusing to execute.\n{sql_query}"

        conn = _get_conn()
        try:
            result = conn.execute(sql_query).fetchall()
            col_names = [desc[0] for desc in conn.description]
        finally:
            conn.close()

        if not result:
            return f"Query returned no results.\nSQL: {sql_query}"

        # Format as a readable table
        output = f"SQL: {sql_query}\n\n"
        output += " | ".join(col_names) + "\n"
        output += "-" * len(" | ".join(col_names)) + "\n"
        for row in result[:20]:
            formatted = []
            for item in row:
                if isinstance(item, float):
                    formatted.append(f"{item:,.2f}")
                elif isinstance(item, int):
                    formatted.append(f"{item:,}")
                elif isinstance(item, date):
                    formatted.append(item.isoformat())
                else:
                    formatted.append(str(item))
            output += " | ".join(formatted) + "\n"
        if len(result) > 20:
            output += f"\n... showing first 20 of {len(result)} total results"
        return output

    except Exception as e:
        return f"Error executing analytics query: {e}"


