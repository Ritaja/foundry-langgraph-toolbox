@tool
def crm_search_name(query: str) -> str:
    """Search insurance customers by name (case-insensitive, partial match).

    Args:
        query: Full or partial customer name to search for.

    Returns:
        JSON array of matching customer profiles with policy counts.
    """
    conn = _get_conn()
    try:
        results = _rows_to_dicts(
            conn.execute(
                """
                SELECT c.customer_id, c.first_name, c.last_name, c.email,
                       c.phone, c.date_of_birth, c.city, c.state,
                       COUNT(DISTINCT p.policy_id) AS total_policies,
                       COUNT(DISTINCT CASE WHEN p.status = 'active' THEN p.policy_id END) AS active_policies
                FROM customers c
                LEFT JOIN policies p ON c.customer_id = p.customer_id
                WHERE LOWER(c.first_name || ' ' || c.last_name) LIKE ?
                GROUP BY c.customer_id, c.first_name, c.last_name, c.email,
                         c.phone, c.date_of_birth, c.city, c.state
                LIMIT 20
                """,
                [f"%{query.lower()}%"],
            )
        )
        if not results:
            return json.dumps({"message": f"No customers found matching '{query}'."})
        return json.dumps(results, indent=2, default=_json_serial)
    finally:
        conn.close()


@tool
def crm_search_id(query: str) -> str:
    """Look up an insurance customer by their exact customer ID.

    Args:
        query: The customer ID to look up (integer as string).

    Returns:
        JSON object with customer profile, policy summaries, and claim summaries.
    """
    conn = _get_conn()
    try:
        cust = _rows_to_dicts(
            conn.execute("SELECT * FROM customers WHERE customer_id = ?", [int(query)])
        )
        if not cust:
            return json.dumps({"message": f"No customer found with ID '{query}'."})

        customer = cust[0]
        customer["policies"] = _rows_to_dicts(
            conn.execute(
                """SELECT policy_id, policy_type, coverage_amount, premium_amount,
                          start_date, end_date, status
                   FROM policies WHERE customer_id = ?""",
                [int(query)],
            )
        )
        customer["claims"] = _rows_to_dicts(
            conn.execute(
                """SELECT cl.claim_id, cl.policy_id, cl.claim_amount, cl.claim_date,
                          cl.claim_status, cl.claim_type
                   FROM claims cl
                   JOIN policies p ON cl.policy_id = p.policy_id
                   WHERE p.customer_id = ?""",
                [int(query)],
            )
        )
        return json.dumps(customer, indent=2, default=_json_serial)
    finally:
        conn.close()


@tool
def crm_get_policies(query: str) -> str:
    """Get full policy details for a customer by their customer ID.

    Args:
        query: The customer ID whose policies to retrieve.

    Returns:
        JSON array with complete policy objects including coverage details.
    """
    conn = _get_conn()
    try:
        results = _rows_to_dicts(
            conn.execute(
                "SELECT * FROM policies WHERE customer_id = ? ORDER BY start_date DESC",
                [int(query)],
            )
        )
        if not results:
            return json.dumps({"message": f"No policies found for customer ID '{query}'."})
        return json.dumps(results, indent=2, default=_json_serial)
    finally:
        conn.close()


@tool
def crm_list_customers() -> str:
    """List insurance customers with a brief summary of each (first 50).

    Returns:
        JSON array of customer summaries with policy counts.
    """
    conn = _get_conn()
    try:
        results = _rows_to_dicts(
            conn.execute(
                """
                SELECT c.customer_id, c.first_name, c.last_name, c.city, c.state,
                       COUNT(DISTINCT p.policy_id) AS total_policies,
                       COUNT(DISTINCT CASE WHEN p.status = 'active' THEN p.policy_id END) AS active_policies
                FROM customers c
                LEFT JOIN policies p ON c.customer_id = p.customer_id
                GROUP BY c.customer_id, c.first_name, c.last_name, c.city, c.state
                ORDER BY c.customer_id
                LIMIT 50
                """
            )
        )
        return json.dumps(results, indent=2, default=_json_serial)
    finally:
        conn.close()


