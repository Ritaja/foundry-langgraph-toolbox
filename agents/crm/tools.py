"""CRM tools — read-only access to insurance data stored in a DuckDB database.

The database is generated by ``generate_synthetic_data.py`` and contains
three tables: customers, policies, and claims.
"""

from __future__ import annotations

import json
import logging
import os
import pathlib
from datetime import date

import duckdb
from langchain_core.tools import tool

logger = logging.getLogger(__name__)

_DB_PATH = os.environ.get(
    "CRM_DB_PATH",
    str(pathlib.Path(__file__).resolve().parent.parent.parent / "insurance_data.db"),
)


def _get_conn() -> duckdb.DuckDBPyConnection:
    """Return a read-only DuckDB connection to the CRM database."""
    return duckdb.connect(_DB_PATH, read_only=True)


def _json_serial(obj: object) -> str:
    """JSON serializer for types not handled by default."""
    if isinstance(obj, date):
        return obj.isoformat()
    raise TypeError(f"Type {type(obj)} not serializable")


def _rows_to_dicts(cursor: duckdb.DuckDBPyConnection) -> list[dict]:
    """Convert DuckDB query results to a list of dicts."""
    cols = [desc[0] for desc in cursor.description]
    return [dict(zip(cols, row)) for row in cursor.fetchall()]


